//Assignment[=]
let x = 20;
console.log(x);//20

//Addition Assignment[+=]
let y = 20;
y += 10; //y = y + 10
console.log(y);

//Subtraction Assignment[-=]
let z = 20;
z -= 10; //z = z -10
console.log(z);

//Multiplication Assignment[*=]
let a = 20;
a *= 10; //a = a * 10
console.log(a); 

//Remaider Assignment
let b = 20;
b %= 10; //b = b % 10
console.log(b); 

//Exponentiation Assignment
let c = 20;
c **= 2; //c = c **2
console.log(c);